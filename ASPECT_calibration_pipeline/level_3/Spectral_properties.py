# Slope removal using three key points

fit_x = np.array([x1, x2, x3])
fit_y = np.array([y1, y2, y3])
coeffs = np.polyfit(fit_x, fit_y, 1)
trend = np.polyval(coeffs, x)
y_flat = y - trend + np.mean(fit_y)

baseline = np.mean(fit_y)

#calculating band depth and HBW
# Segment 1: x1 to x2
seg1_mask = (x >= x1) & (x <= x2)
x_seg1 = x[seg1_mask]
y_seg1 = y_flat[seg1_mask]
depth1 = baseline - np.min(y_seg1)
half_depth1 = baseline - depth1 / 2
left_idx1 = np.where(y_seg1 <= half_depth1)[0][0]
right_idx1 = np.where(y_seg1 <= half_depth1)[0][-1]
hbw1 = x_seg1[right_idx1] - x_seg1[left_idx1]

# Segment 2: x2 to x3
seg2_mask = (x >= x2) & (x <= x3)
x_seg2 = x[seg2_mask]
y_seg2 = y_flat[seg2_mask]
