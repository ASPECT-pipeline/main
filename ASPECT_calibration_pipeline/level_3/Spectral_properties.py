# Find maximum points in specified ranges
mask1 = (x >= 500) & (x <= 1000)
mask2 = (x >= 1400) & (x <= 1500)

x1 = x[mask1][np.argmax(y[mask1])]
y1 = y[mask1][np.argmax(y[mask1])]

x2 = x[mask2][np.argmax(y[mask2])]
