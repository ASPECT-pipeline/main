half_depth2 = baseline - depth2 / 2
left_idx2 = np.where(y_seg2 <= half_depth2)[0][0]
right_idx2 = np.where(y_seg2 <= half_depth2)[0][-1]
hbw2 = x_seg2[right_idx2] - x_seg2[left_idx2]

